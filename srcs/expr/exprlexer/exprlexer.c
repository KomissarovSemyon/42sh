static int			g_exprstate = 1;

char	*exprdfa(char **str, char **string)
{
	char	*lex;
	int		state;
	int		i;

	i = 0;
	state = 1;
	lex = NULL;
	while ((*str)[i])
	{
		g_exprstate = state;
		if ((*str)[i] < 0 || (*str)[i] > 127)
			state = g_exprbranch_table[128][state];
		else
			state = g_exprbranch_table[(int)((*str)[i])][state];
		if (!state)
		{
			lex = push_symbol(string, *str, (size_t)i);
			*str = &((*str)[i]);
			return (lex);
		}
		i++;
	}
	if (i)
	{
		g_exprstate = state;
		lex = push_symbol(string, *str, (size_t)i);
		*str = &((*str)[i]);
		return (lex);
	}
	return (lex);
}

void	build_exprlexems(char *str, void **lexems, char *string)
{
	char	*lex;

	while ((lex = exprdfa(&str, &string)))
		push_token(lexems, lex, g_exprstate);
}

t_lexer	*exprlexer(char *str, size_t len)
{
	t_lexer		*lexer;
	void 		*lexems;
	char		*string;

	if (!(lexems = vector_create(sizeof(t_lexem))))
		die();
	string = (char *)xmalloc(sizeof(char) * len * 2);
	build_exprlexems((char *)str, &lexems, string);
	lexer = (t_lexer *)xmalloc(sizeof(t_lexer));
	lexer->lexems = lexems;
	lexer->symbol_table = string;
	return (lexer);
}


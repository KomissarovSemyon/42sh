#include "shell.h"

int 	keyvaluecmp(char *el1, char *el2)
{
	while (*el1 && *el2 && *el1 != '=')
	{
		if (*el1 != *el2)
			return (0);
		el1++;
		el2++;
	}
	if (!*el1 || (*el1 == '=' && !*(el1 + 1)))
		return (0);
	return (1);
}

#include <string.h>
#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	size_t i;

	if (!dest || !src)
		return ((char *)0);
	i = ft_strlen(src);
	ft_memcpy(dest, src, (i > n ? n : i));
	if (i > n)
		ft_memset(dest, 0, i - n);
	return (dest);
}

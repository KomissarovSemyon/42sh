#include <string.h>

char	*ft_strrchr(const char *s, int c)
{
	long i;

	i = 0;
	while (s[i])
		i++;
	if (!c)
		return ((char *)s + i);
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		i--;
	}
	return (NULL);
}

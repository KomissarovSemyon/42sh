int		g_i = 0;
int		g_k = 0;

void	stack_push(char *stack, char c)
{
	stack[g_i] = c;
	g_i++;
}

void	stack_move(char *stack, size_t len, char *dest)
{
	int		i;

	i = 0;
	if (lq)
		dest[g_k++] = lq;
	while (i <= g_i)
	{
		dest[g_k] = stack[i];
		g_k++;
		i++;
	}
	g_k--;
	if (lq)
		dest[g_k++] = lq;
	ft_bzero(stack, len);
	g_i = 0;
}

int		is_quote(char c)
{
	return (c == '\'' || c == '\"');
}

char	*quotes_handler(char *str, size_t len, char *stack)
{
	char	*dest;
	int		state;
	int		i;

	i = 0;
	state = 1;
	dest = (char *)malloc(len + 1);
	while (i < len)
	{
		state = g_bt[(int)(str[i])][state];
		if (state == 4)
			lq = str[i];
		if (state == 11)
		{
			state = 4;
			i++;
			continue ;
		}
		ft_putnbrendl(state);
		stack_push(stack, str[i]);
		if (!state)
		{
			stack_move(stack, len, dest);
			state = 1;
		}
		i++;
	}
	stack_move(stack, len, dest);
	return (dest);
}

char	*trim_quotes(char *str, size_t len)
{
	char	*stack;

	stack = (char *)malloc(len);
	return (quotes_handler(str, len, stack));
}

int		main(int ac, char **av)
{
	char	*dest;

	if (ac != 3)
		return (1);
	if (!(dest = trim_quotes(av[1], ft_atoi(av[2]))))
		return (1);
	ft_putendl(dest);
	return (0);
}

